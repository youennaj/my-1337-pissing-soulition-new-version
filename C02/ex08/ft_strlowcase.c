char *ft_strlowcase(char *str) 
{
    int i = 0 ;
    while (str[i] != '\0')
    {
        if (str[i] >= 'A' && str[i] <= 'Z')
        //     104 >= 97 &&    104 <= 122 = true 
        {
            str[i] += 32;
            // 104 - 32 = 72 = H 
            // 101 - 32 = 69 = E
        }
        
        i++;
    }
    return str;
}

