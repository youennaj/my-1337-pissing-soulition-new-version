#include <unistd.h>

void ft_putchar(char c) {
    write(1, &c, 1);
}

void ft_putstr(char *str) {//"hello\0"
    int i = 0;
    while(str[i] != '\0') {
        ft_putchar(str[i]);
        i++;
    }
}
